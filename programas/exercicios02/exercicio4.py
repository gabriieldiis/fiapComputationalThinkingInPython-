# Dados 3 lados de um triângulo, calcule e mostre seu perímetro (soma dos lados)

lado1 = float(input(" Digite o valor do primeiro lado do seu triangulo"))
lado2 = float(input(" Digite o valor do segundo lado do seu triangulo"))
lado3 = float(input(" Digite o valor do terceiro lado do seu triangulo"))

p = ( lado1 + lado2 + lado3 )

print("Seu triangulo tem " , p , "de perimetro")